export interface BitmapFont {
  width: number;
  height: number;
  spacing?: number;
  spaceWidth?: number;
  glyphs: Record<string, number[]>;
}

const fonts: Map<string, BitmapFont> = new Map();
let defaultSize = "5x7";

export function registerFont(name: string, font: BitmapFont) {
  fonts.set(name, font);
}

export function getFont(size?: string): BitmapFont {
  if (size && fonts.has(size)) return fonts.get(size)!;
  return fonts.get(defaultSize) ?? fonts.values().next().value!;
}

export function setDefaultFont(name: string) {
  defaultSize = name;
}

